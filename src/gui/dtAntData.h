using namespace std;

struct AntPoint: Moveable<AntPoint>{
	double angle;
	double attenuation;
	void Clear(){
		angle = 0.0;
		attenuation = 0.0;
	}
	String ToPrint()const{
		return Format("\t angle: %f\t attenuation: %f\n", angle, attenuation);
	}
	String ToString()const{
		return Format("\t\t\t\t=\t%f\t%f\n", angle, attenuation);
	}
	std::string ToStdString()const{
		std::stringstream ss;
		ss << "\t angle: " << to_string(angle) << "\t attenuation: " << to_string(attenuation) << "\n";
		return ss.str();
	}
	AntPoint(){Clear();}
	AntPoint(const double angle, const double attenuation):angle(angle), attenuation(attenuation){}
	AntPoint(const AntPoint& point): angle(point.angle), attenuation(point.attenuation){}
};
//! --------------------------------------------------------------------------------------------

// Class of standard antenna data
class TStdAnt: Moveable<TStdAnt>{
private:
	int err;
	void Initialise();
	static const double freq_table[301][13];
	static const double att_table[301][13];
	struct AntRec: Moveable<AntRec>{
		int gain;
		Vector<AntPoint> cpPoints;
		Vector<AntPoint> xpPoints;
		void Clear(){gain=0; cpPoints.Clear(); xpPoints.Clear();}
		String ToString()const{
			String cpStr = "\ncpPoints:\n";
			for(auto p: cpPoints)
				cpStr << "\t" << p.ToString();
			String xpStr = "\nxpPoints:\n";
			for(auto p: xpPoints)
				xpStr << "\t" << p.ToString();
			return Format("\n\ngain: %d\n %s %s", gain, cpStr, xpStr);
		};
		AntRec(){Clear();}
		AntRec(const AntRec& ant): gain(ant.gain), cpPoints(clone(ant.cpPoints)), xpPoints(clone(ant.xpPoints)){}
	};
	Vector<AntRec> _antList;
public:
	// int Error(){return err;};
	int CpPattern(double gain, Vector<AntPoint>& pattern)const;
	int XpPattern(double gain, Vector<AntPoint>& pattern)const;
	String ToString()const;
	TStdAnt(){
		err = 0;
		Initialise();
	}

};
//!---------------------------------------------------------------------------------------------


void TStdAnt::Initialise(){
	_antList.Clear();
	for(int i = 0; i <= 300; i++){
		AntRec stdAnt;
		stdAnt.gain = 200 + i;
		for(int j = 0; j < 13; j++){
			AntPoint antdata(freq_table[i][j], att_table[i][j]);
			stdAnt.cpPoints.Add(antdata);
			if(j==0){
				// First point of XP data
				if(stdAnt.gain >=200 && stdAnt.gain <=229) antdata.attenuation = 15;
				else if(stdAnt.gain >=230 && stdAnt.gain <=269) antdata.attenuation = 20;
				else if(stdAnt.gain >=270 && stdAnt.gain <=299) antdata.attenuation = 25;
				else if(stdAnt.gain >=300 && stdAnt.gain <=500) antdata.attenuation = 30;
			}
			// Other points of XP data
			if(stdAnt.gain >=200 && stdAnt.gain <=229 && antdata.attenuation >= 15){
				stdAnt.xpPoints.Add(antdata);
			}
			else if(stdAnt.gain >=230 && stdAnt.gain <=269 && antdata.attenuation >= 20){
					stdAnt.xpPoints.Add(antdata);
			}
			else if(stdAnt.gain >=270 && stdAnt.gain <=299 && antdata.attenuation >= 25){
				stdAnt.xpPoints.Add(antdata);
			}
			else if(stdAnt.gain >=300 && stdAnt.gain <=500 && antdata.attenuation >= 30){
				stdAnt.xpPoints.Add(antdata);
			}
		}
		_antList.Add(clone(stdAnt));
	}
}

int TStdAnt::CpPattern(double gain, Vector<AntPoint>& pattern)const{
	bool found = false;
	for(int i = 0; i < _antList.size(); i++){
		long gn = static_cast<long>(gain * 10);
		if(gn == _antList[i].gain){
			pattern = clone(_antList[i].cpPoints);
			found = true;
		}
	}
	if(!found)
		return 3;
	else
		return 0;
}

int TStdAnt::XpPattern(double gain, Vector<AntPoint>& pattern)const{
	bool found = false;
	for(int i = 0; i < _antList.size(); i++){
		int gn = static_cast<int>(gain * 10);
		if(gn == _antList[i].gain){
			pattern = clone(_antList[i].xpPoints);
			found = true;
		}
	}
	if(!found)
		return 3;
	else
		return 0;
}
