// Creates missing side of the mask
//
//	--					    ----
//	  \					   /    \
//	   \		-->		  /      \
//	    --				--        --
//
void TNfd::CorrectMask(vector<TrMask>& maskIn, vector<TrMask>& maskOut){
	maskOut.clear();
	int start = static_cast<int>(maskIn.size()) - 1;
	for(int i = start; i >= 0; i--){
		TrMask msk;
		msk.attenuation = maskIn[i].attenuation;
		msk.frequency	= - maskIn[i].frequency;
		maskOut.push_back(msk);
	}
	if(maskIn[0].frequency == 0)
		start = 1;
	else{
		start = 0;
		TrMask msk;
		msk.attenuation = 0;
		msk.frequency	= 0;
		maskOut.push_back(msk);
	}
	for(int i = start; i < maskIn.size(); i++){
		TrMask msk;
		msk.attenuation = maskIn[i].attenuation;
		msk.frequency	= maskIn[i].frequency;
		maskOut.push_back(msk);
	}
}
//!---------------------------------------------------------------------------------------------

bool Sorting(const TrMask& i, const TrMask& j){
	return i.frequency < j.frequency;
}
//!---------------------------------------------------------------------------------------------

void TNfd::SetTxMask(vector <TrMask> &mask){
	std::sort( mask.begin(), mask.end(), Sorting);
	CorrectMask(mask, txmask);
}
