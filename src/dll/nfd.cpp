// Calculation of flat element
// Annex 3B, formulae 2.1
// Frequency in MHz
double TNfd::FlatElement(double TxdB, double RxdB, double dF){
	return dF * pow(10,-(TxdB + RxdB)/10);
}
//!---------------------------------------------------------------------------------------------

// Calculation of slope element
// Annex 3B, formulae 2.2
// Frequency in MHz
double TNfd::SlopeElement(double Tx1dB, double Rx1dB, double Tx2dB, double Rx2dB, double dF){
	double b = Tx2dB + Rx2dB;
	double a = (Tx1dB +Rx1dB - b)/dF;
	double p1 = pow(10 , -b / 10);
	double p2 = (log(10.0) / 10) * a;
	double p3 = (1 - pow(10 , -a * dF / 10));
	return  p1 * p3 / p2;
}
//!---------------------------------------------------------------------------------------------

// Calculation of Tx area
double TNfd::TxArea(){
	double area = 0;
	for(int i = 1; i < txmask.size(); i++)
		if(txmask[i].attenuation == txmask[i-1].attenuation)
			area = area + FlatElement(txmask[i-1].attenuation, 0, fabs(txmask[i-1].frequency - txmask[i].frequency));
		else
			area = area + SlopeElement(txmask[i-1].attenuation, 0, txmask[i].attenuation, 0, fabs(txmask[i-1].frequency - txmask[i].frequency));
	return area;
}
