// Function creates default TX mask according to frequency band, class and channel spacing
// Valid classes are 1, 2, 3, 4H, 4L, 5A, 5B, 6A, 6B
// Internally letters in the name of classes are substituted by numbers:
// A = 1, B = 2, H = 3, L = 4
void TNfd::SetTxMask(double frequency_GHz, string eqclass, double channelsp){
	error = 0;
	txmask.clear();
	// Search for the beginning of the respective frequency band
	int frequency_band_index = -1;
	for(int i = 0; i < 165; i++){
		if(frequency_GHz > txmask_table[i][0] - 0.000001 && frequency_GHz < txmask_table[i][1] + 0.000001){
			// 0.000001 is to eliminate convertion error
			frequency_band_index = i;
			break;
		}
	}
	if(frequency_band_index == -1){
		error = 3;
		return;
	}
	// Change class name (1, 2, 4L, 4H, 5A, 5B, 6A, 6B) from string to double according to format of txmask_table
	double dclass;
	if(eqclass == "1") dclass = 1;
	else
		if(eqclass == "2") dclass = 2;
		else
			if(eqclass == "4L" || eqclass == "4l") dclass = 44;
			else
				if(eqclass == "4H" || eqclass == "4h") dclass = 43;
				else
					if(eqclass == "5A" || eqclass == "5a") dclass = 51;
					else
						if(eqclass == "5B" || eqclass == "5b") dclass = 52;
						else
							if(eqclass == "6A" || eqclass == "6a") dclass = 61;
							else
								if(eqclass == "6B" || eqclass == "6b") dclass = 62;
								else
									if(eqclass == "") dclass = -1;
									else{
										error = 2;
										return;
									}
	bool found = false;
	while(frequency_GHz > txmask_table[frequency_band_index][0] - 0.000001 && frequency_GHz < txmask_table[frequency_band_index][1] + 0.000001){
		if (dclass == -1){
			// When channel spacing is not defind, search for lowest available class with the appropriate channel spacing
			// (this means firs occurence of appropriate channel spacing)
			if(channelsp <= txmask_table[frequency_band_index][4]){
				found = true;
				break;
			}
		}
		else
			if(dclass == txmask_table[frequency_band_index][2] &&
				channelsp <= txmask_table[frequency_band_index][4]){
				found = true;
				break;
			}
		frequency_band_index++;
		if (frequency_band_index > 164){
			error = 3;	// no tx default mask data found
			break;		// end of mask table reached
		}
	}
	if(!found){
		error = 3;
		return;
	}

	// Create tx mask vector
	// 1. Create point with frequency (-1) * 3.5 * channels_spacing
	TrMask pt;
	pt.frequency = -1 * 3.5 * channelsp;
	pt.attenuation = txmask_table[frequency_band_index][16] + 5;
	txmask.push_back(pt);
	// 2. Create point with frequency (-1) * 2.5 * channels_spacing
	if(channelsp * 2.5 > txmask_table[frequency_band_index][15]){
		// this check is just for precaution
		TrMask pt;
		pt.frequency = -1 * 2.5 * channelsp;
		pt.attenuation = txmask_table[frequency_band_index][16];
		txmask.push_back(pt);
	}
	// 3. Create mask part with negative frequencies
	for(int i = 16; i > 4; i--){
		TrMask pt;
		pt.attenuation = txmask_table[frequency_band_index][i];
		i--;
		pt.frequency = -1 * txmask_table[frequency_band_index][i];
		txmask.push_back(pt);
	}
	// 4. Create mask central point
	pt.frequency = 0;
	pt.attenuation = 0;
	txmask.push_back(pt);
	// 5. Create mask part with positive frequencies
	for(int i = 5; i < 17; i++){
		TrMask pt;
		pt.frequency = txmask_table[frequency_band_index][i];
		i++;
		pt.attenuation = txmask_table[frequency_band_index][i];
		txmask.push_back(pt);
	}
	// 6. Create point with frequency 2.5 * channels_spacing
	if(channelsp * 2.5 > txmask_table[frequency_band_index][15]){
		// this check is just for precaution
		TrMask pt;
		pt.frequency = 2.5 * channelsp;
		pt.attenuation = txmask_table[frequency_band_index][16];
		txmask.push_back(pt);
	}
	// 7. Create point with frequency 3.5 * channels_spacing
	pt.frequency = 3.5 * channelsp;
	pt.attenuation = txmask_table[frequency_band_index][16] + 5;
	txmask.push_back(pt);
}
