// Creates array of mask elements for calculation of overlapping area
void TNfd::CreateElements(double offset, vector <Element>& mask_items){
	//Collect all points from TX mask
	for(int i = 0; i < txmask.size(); i++){
		if(txmask[i].frequency >= rxmask[0].frequency + offset && txmask[i].frequency <= rxmask[rxmask.size()-1].frequency + offset){
			Element element;
			element.frequency	= txmask[i].frequency;
			element.txAtt		= txmask[i].attenuation;
			// Interpolate appropriate point form RX mask
			int j = 0;
			while(element.frequency > rxmask[j].frequency + offset) j++;
			if(j == 0)
				element.rxAtt = rxmask[j].attenuation;
			else
				element.rxAtt = (rxmask[j].attenuation - rxmask[j-1].attenuation) *
								(element.frequency - (rxmask[j-1].frequency + offset)) /
								(rxmask[j].frequency - rxmask[j-1].frequency) + rxmask[j-1].attenuation;
			mask_items.push_back(element);
		}
	}
	// Collect all points from RX mask
	for(int i = 0; i < rxmask.size(); i++){
		if(rxmask[i].frequency + offset >= txmask[0].frequency && rxmask[i].frequency + offset <= txmask[txmask.size()-1].frequency){
			Element element;
			element.frequency	= rxmask[i].frequency + offset;
			element.rxAtt		= rxmask[i].attenuation;
			int j = 0;
			while(element.frequency > txmask[j].frequency) j++;
			if(j == 0)
				element.txAtt = txmask[j].attenuation;
			else
				element.txAtt = (txmask[j].attenuation - txmask[j-1].attenuation) *
								(element.frequency - txmask[j-1].frequency) /
								(txmask[j].frequency - txmask[j-1].frequency) + txmask[j-1].attenuation;
			mask_items.push_back(element);
		}
	}
	// Sort array of elements
	for(int i = 0; i < mask_items.size(); i++){
		for(int j = i+1; j < mask_items.size(); j++){
			if(mask_items[j].frequency < mask_items[i].frequency){
				Element element = mask_items[i];
				mask_items[i] = mask_items[j];
				mask_items[j] = element;
			}
		}
	}
}
