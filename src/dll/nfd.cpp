// Calculation of overlapping area
double TNfd::OverlappingArea(vector <Element>& mask_items){
	double oa = 0;
	if(mask_items.size() <= 1){
		error = 1; // masks do not overlap
		return 0;
	}
	for(int i = 0; i < mask_items.size()-1; i++){
		if(fabs(mask_items[i+1].frequency - mask_items[i].frequency) > 0){
			// This is a failsafe to avoid division by zero
			if(fabs(mask_items[i].txAtt + mask_items[i].rxAtt - mask_items[i+1].txAtt - mask_items[i+1].rxAtt) < 1e-8)
				oa = oa + FlatElement(mask_items[i].txAtt, mask_items[i].rxAtt, fabs(mask_items[i+1].frequency - mask_items[i].frequency));
			else
				oa = oa + SlopeElement(mask_items[i].txAtt, mask_items[i].rxAtt,mask_items[i+1].txAtt,
					 mask_items[i+1].rxAtt,fabs(mask_items[i+1].frequency - mask_items[i].frequency));
		}
	}
	return oa;
}
//!---------------------------------------------------------------------------------------------

// Calculation of Net Filter Diskrimination
double TNfd::NFD(double offset, double& overlap_area, double& overlap_area_co){
	vector <Element> mask_items_cc;
	vector <Element> mask_items_fo;
	CreateElements(0.0, mask_items_cc);		// cochannel
	CreateElements(offset,mask_items_fo);	// with frequency offset
	overlap_area_co = OverlappingArea(mask_items_cc);	// cochannel
	overlap_area = OverlappingArea(mask_items_fo);		// with frequency offset
	if(error == 1 || overlap_area == 0){
		overlap_area = 0;
		return 0; // if no overlap return
	}
	return 10 * log10(overlap_area_co / overlap_area);
}
//!---------------------------------------------------------------------------------------------

// Calculation of Mask Diskrimination
double TNfd::MD(double& tx_area){
	vector <Element> mask_items_cc;
	CreateElements(0, mask_items_cc);
	tx_area = TxArea();
	double co_area = OverlappingArea(mask_items_cc);
	return 10 * log10(tx_area / co_area);
}
